package dao;

public class Petinfo {
	
	private int mem_num;
	private int pet_num;
	private String pet_name;
	private String pet_gender;
	private String pet_type;
	private int pet_age;
	private int pet_weight;
	private String pet_neuter;
	private String pet_photo;
	
	public int getMem_num() {
		return mem_num;
	}
	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}
	public int getPet_num() {
		return pet_num;
	}
	public void setPet_num(int pet_num) {
		this.pet_num = pet_num;
	}
	public String getPet_name() {
		return pet_name;
	}
	public void setPet_name(String pet_name) {
		this.pet_name = pet_name;
	}
	public String getPet_gender() {
		return pet_gender;
	}
	public void setPet_gender(String pet_gender) {
		this.pet_gender = pet_gender;
	}
	public String getPet_type() {
		return pet_type;
	}
	public void setPet_type(String pet_type) {
		this.pet_type = pet_type;
	}
	public int getPet_age() {
		return pet_age;
	}
	public void setPet_age(int pet_age) {
		this.pet_age = pet_age;
	}
	public int getPet_weight() {
		return pet_weight;
	}
	public void setPet_weight(int pet_weight) {
		this.pet_weight = pet_weight;
	}
	public String getPet_neuter() {
		return pet_neuter;
	}
	public void setPet_neuter(String pet_neuter) {
		this.pet_neuter = pet_neuter;
	}
	public String getPet_photo() {
		return pet_photo;
	}
	public void setPet_photo(String pet_photo) {
		this.pet_photo = pet_photo;
	}
	
	

}
