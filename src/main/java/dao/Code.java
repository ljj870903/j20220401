package dao;

public class Code {
	
	private String bcd;
	private String mcd;
	private String name;
	
	public String getBcd() {
		return bcd;
	}
	public void setBcd(String bcd) {
		this.bcd = bcd;
	}
	public String getMcd() {
		return mcd;
	}
	public void setMcd(String mcd) {
		this.mcd = mcd;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
